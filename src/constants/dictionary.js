const generateDictionary = () => {
  let list = [...USER_FAVORITES];
  const prefixes = ["و", "ف", "ب", "ل", "ك"];
  
  ROOTS.forEach(([wrong, right]) => {
    // إدراج الكلمة المجردة
    list.push([wrong, right]); 
    // توليد الكلمات مع السوابق (واسهل، فافضل، بالاضافة...)
    prefixes.forEach(p => {
      list.push([p + wrong, p + right]);
    });
  });
  
  // ترتيب المصفوفة من الأطول إلى الأقصر لضمان دقة الاستبدال
  return list.sort((a, b) => b[0].length - a[0].length);
};

