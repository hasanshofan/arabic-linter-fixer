// 1. الكلمات الثابتة (أولويات المستخدم)
const USER_FAVORITES = [
  ["اذا", "إذا"], ["الى", "إلى"], ["اي", "أي"], ["الان", "الآن"], ["انا", "أنا"],
  ["او", "أو"], ["فان", "فإن"], ["بالاضافة", "بالإضافة"],["الاخر","الآخر"],["اخيرا","أخيراً"],
  ["اضافة", "إضافة"], ["ايضا", "أيضاً"], ["أيضا", "أيضاً"], ["شكرا", "شكراً"], ["كثيرا", "كثيراً"],
  ["اخر","آخر"],
  ["الانسان","الإنسان"]
  
  
];

// 2. الجذور التي يتولد منها مئات الكلمات
const ROOTS = [
  ["اولا", "أولاً"], ["ثانيا", "ثانياً"], ["ثالثا", "ثالثاً"], ["رابعا", "رابعاً"],
  ["افضل", "أفضل"], ["اسهل", "أسهل"], ["اكثر", "أكثر"], ["اكبر", "أكبر"],
  ["اقل", "أقل"], ["اعلى", "أعلى"], ["اسفل", "أسفل"], ["اهم", "أهم"],
  ["ايمان", "إيمان"], ["اخطاء", "أخطاء"], ["ارقام", "أرقام"], ["اعمال", "أعمال"],
  ["اعلان", "إعلان"], ["اعلام", "إعلام"], ["امس", "أمس"], ["اصبح", "أصبح"],
  ["ادارة", "إدارة"], ["ارادة", "إرادة"], ["اعادة", "إعادة"], ["اسامة", "أسامة"],
  ["احمد", "أحمد"], ["اشياء", "أشياء"], ["اسماء", "أسماء"],
  ["انتاج", "إنتاج"], ["انسان", "إنسان"], ["امكانية", "إمكانية"], ["انجاز", "إنجاز"]
];

const generateDictionary = () => {
  let list = [...USER_FAVORITES];
  const prefixes = ["و", "ف", "ب", "ل", "ك"];
  
  ROOTS.forEach(([wrong, right]) => {
    // إدراج الكلمة المجردة
    list.push([wrong, right]); 
    // توليد الكلمات مع السوابق (واسهل، فافضل، بالاضافة...)
    prefixes.forEach(p => {
      list.push([p + wrong, p + right]);
    });
  });
  
  // ترتيب المصفوفة من الأطول إلى الأقصر لضمان دقة الاستبدال
  return list.sort((a, b) => b[0].length - a[0].length);
};

export const FINAL_DICTIONARY = generateDictionary();
